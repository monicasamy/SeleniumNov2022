package week1.day2;

public class Factorial {

	public static void main(String[] args) {
	
		  int Factorial = 1;  
		  int Number = 4;  
		  for(int i=1;i<=Number;i++)
		  {    
		      Factorial=Factorial*i;    
		  }    
		  System.out.println("Factorial of the number is"+ " " + Factorial);    
		 }  

}
